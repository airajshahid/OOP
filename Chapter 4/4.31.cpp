#include<iostream>
#include<math.h>
using namespace std;
int main()
{
    float r,pi=3.14159,c,d,a;
    cout<<"eter radius of circle : ";
    cin>>r;
    d=r*2;
    c=2*pi*r;
    a=pi*r*r;
    cout<<" diameter of circle is :"<<d<<"\n";
    cout<<" circumference of circle is :"<<c<<"\n";
    cout<<" area of circle is :"<<a<<"\n";
}
